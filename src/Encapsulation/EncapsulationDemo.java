
package Encapsulation;


public class EncapsulationDemo {
    private String Name;
    private int age;
    
     
      public void setName(String Name){
          this.Name=Name;
        
      } 
       public String getName(){
           return Name;
      }
       public void setage(int age){
           this.age=age;
       }
       public int getage(){
           return age;
       }
      
    
    
}
